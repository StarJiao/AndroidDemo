package com.star.bean;

public class BaseInfo {
    /**
     * 时间戳
     */
    public String timestamp;

    /**
     * 接口流水号
     */
    public String flowmark;

    /**
     * 手机序列号
     */
    public String imei;

    /**
     * 有效期
     */
    public String expire;

    /**
     * 权限令牌
     */
    public String token;

    /**
     * 响应码
     */
    public String code;

    /**
     * 响应描述
     */
    public String description;
}
